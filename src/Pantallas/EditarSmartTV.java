    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        String marca = txtmarca.getText();
        String modelo = txtmodelo.getText();
        int tipo =  cbxPuerto.getSelectedIndex();
        
        if(!"".equals(marca)&&!"".equals(modelo)&&!"".equals(tipo)){
            if(JOptionPane.showConfirmDialog(this,"Â¿Deseas Guardar?")==
                    JOptionPane.OK_OPTION){
                smart = new SmartTv();
                smart.setMarca(marca);
                smart.setModelo(modelo);       
                smart.setTipoConexion(tipo);
                padre.addSmartTV(smart);
                this.setVisible(false);
                this.dispose();
                padre.actualizarLista();
                padre.setVisible(true);

            }
                    }else{
            JOptionPane.showMessageDialog(rootPane, "Complete todos los campos"
                    ,"Agregar Celular", HEIGHT);
        }
    }//GEN-LAST:event_btnguardarActionPerformed

